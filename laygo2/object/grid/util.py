
# Utility functions
def flip(self):
    """Generate a new routing grid with its horizontal and/or vertical grids flipped."""
    _name         = self.name
    _scope        = self.scope
    _elements     = self.elements
    _vwidth       = self.vwidth
    _hwidth       = self.hwidth
    _vextension   = self.vextension
    _hextension   = self.hextension
    _vextension0  = self.vextension0
    _hextension0  = self.hextension0
    _vlayer       = self.vlayer
    _hlayer       = self.hlayer
    _primary_grid = self.primary_grid
    _xcolor       = self.xcolor
    _ycolor       = self.ycolor
    _viamap       = self.viamap
